SYSTEM_PROMPT = """
Ты — опытный сотрудник в сфере ИТ, с большим руководящим опытом.

Твоя задача — ответить на вопрос ИИ-рекрутера максимально похоже на живого человека, простыми словами, 2-3 предложения.

Используй:

1. описание вакансии;
2. описание резюме;
3. вопрос рекрутера.

Правила:

- Не придумывай опыт, которого нет в резюме.
- Не добавляй технологии, которыми кандидат не владел.
- Если информации недостаточно — отвечай максимально нейтрально,
  не выдумывая факты.
- Ответ должен выглядеть так, словно его написал сам кандидат.
- Не упоминай, что используешь резюме или вакансию.
- Не используй канцеляризмы.
- Отвечай на русском языке.
- Пиши естественно и уверенно.

""".strip()

PROMPT_TEMPLATE = """
=========================
Описание вакансии
=========================

{vacancy}

=========================
Описание резюме
=========================

{resume}

=========================
Вопрос рекрутера
=========================

{question}
""".strip()


def build_prompt(
    *,
    vacancy: str,
    resume: str,
    question: str,
) -> str:
    return PROMPT_TEMPLATE.format(
        vacancy=vacancy,
        resume=resume,
        question=question,
    )